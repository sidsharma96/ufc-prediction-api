"""System prediction model."""

import uuid
from typing import TYPE_CHECKING

from sqlalchemy import Boolean, ForeignKey, Index, Numeric, String, UniqueConstraint
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.db.base import Base, TimestampMixin

if TYPE_CHECKING:
    from app.db.models.fight import Fight
    from app.db.models.fighter import Fighter


class SystemPrediction(Base, TimestampMixin):
    """System-generated fight prediction model.

    Stores predictions generated by our prediction engine,
    along with feature values and algorithm metadata for analysis.
    """

    __tablename__ = "system_predictions"

    # Fight relationship
    fight_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("fights.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # Prediction
    predicted_winner_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("fighters.id"),
        nullable=False,
    )
    confidence: Mapped[float] = mapped_column(
        Numeric(5, 2),
        nullable=False,
        comment="Prediction confidence 0-100",
    )

    # Probability breakdown
    fighter1_win_probability: Mapped[float | None] = mapped_column(
        Numeric(5, 4),
        comment="Probability fighter1 wins (0-1)",
    )
    fighter2_win_probability: Mapped[float | None] = mapped_column(
        Numeric(5, 4),
        comment="Probability fighter2 wins (0-1)",
    )

    # Method predictions (optional)
    ko_probability: Mapped[float | None] = mapped_column(
        Numeric(5, 4),
        comment="Probability of KO/TKO finish",
    )
    submission_probability: Mapped[float | None] = mapped_column(
        Numeric(5, 4),
        comment="Probability of submission finish",
    )
    decision_probability: Mapped[float | None] = mapped_column(
        Numeric(5, 4),
        comment="Probability of decision",
    )

    # Algorithm metadata
    algorithm_version: Mapped[str] = mapped_column(
        String(20),
        nullable=False,
        comment="Version of prediction algorithm (e.g., 'rule_v1', 'ml_v2')",
    )
    feature_weights: Mapped[dict | None] = mapped_column(
        JSONB,
        comment="Weights used for this prediction",
    )
    feature_values: Mapped[dict | None] = mapped_column(
        JSONB,
        comment="Feature values used for debugging and analysis",
    )

    # Result tracking
    is_correct: Mapped[bool | None] = mapped_column(Boolean, index=True)

    # Relationships
    fight: Mapped["Fight"] = relationship("Fight", back_populates="system_predictions")
    predicted_winner: Mapped["Fighter"] = relationship("Fighter")

    # Constraints
    __table_args__ = (
        UniqueConstraint(
            "fight_id",
            "algorithm_version",
            name="uq_fight_algorithm_prediction",
        ),
        Index(
            "idx_system_predictions_accuracy",
            "is_correct",
            postgresql_where="is_correct IS NOT NULL",
        ),
    )

    def __repr__(self) -> str:
        return f"<SystemPrediction {self.fight_id} ({self.algorithm_version})>"
